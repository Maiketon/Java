public class CaracteresEspaciales {

    static void main(String[] args) {
        System.out.println("Clase de caracteres especiales");



        // Tabulador '\t'
        var cadena2 = "\tHola\tMundo";
        System.out.println("cadena2 = " + cadena2);

        // CARACTER ESCAPE  -- \ --, podemos combinarlo para los demas caracteres especiales

        // \' va agregar una comllia simple

        var cadena3="\'Hola Mundo \'";
        System.out.println("cadena3 = " + cadena3);





    }
}
